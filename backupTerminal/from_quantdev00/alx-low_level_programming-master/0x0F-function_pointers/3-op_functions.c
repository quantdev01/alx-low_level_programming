#include <stdlib.h>
/* My functions */

/**
 * op_add - function addition
 *
 * @a: num1
 * @b: num 2
 * Return: an int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function sub
 *
 * @a: num1
 * @b: num 2
 * Return: an int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function mult
 *
 * @a: num1
 * @b: num 2
 * Return: an int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function addition
 *
 * @a: num1
 * @b: num 2
 * Return: an int
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function addition
 *
 * @a: num1
 * @b: num 2
 * Return: an int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
